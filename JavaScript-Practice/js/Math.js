//Math.PI lấy giá trị pi
console.log(Math.PI);

//ví dụ tính diện tích hình tròn
let radius = 5;
let area = Math.PI * radius * radius;
console.log(area);

//console.random() lấy số ngẫu nhiên
let random = Math.floor(Math.random() * 10);
console.log(random);

//Math.floor làm tròn xuống
console.log(Math.floor(5.9));

//Math.ceil làm tròn lên
console.log(Math.ceil(8.1));

//math.round() làm tròn theo quy tắt thông thường
console.log(Math.round(4.7));

//Math.Max tìm giá trị lớn nhất
console.log(Math.max(7, 8, 4, 7, 3, 1));

//Math.min() tìm giá trị  nhỏ nhất
console.log(Math.min(3, 6, 32, 7, 3, 6, 1, 0));

//Math.pow()  lũy thừa
console.log(Math.pow(2, 3)); //nghĩa là 2 mũ 3

//Math.sqrt() căn bậc 2
console.log(Math.sqrt(81));

//Math.abs() giá trị tuyệt đối
console.log(Math.abs(-9));

//Math.cbrt căn bậc ba
console.log(Math.cbrt(27));
